def kaart_clear(x,y):
	global kaart
	if kaart[y-1] [x-1] == "." and player_xy() == [x,y]:
		kaart1 = kaart[y-1]
		kaart1 = list(kaart1)
		kaart1[x] = str(" ")
		kaart[y-1] = kaart1
		kaart[y-1] = "".join(kaart[y-1])

def kaart_xy(x,y):
	global kaart
	return kaart[y-1] [x-1] != '#'
